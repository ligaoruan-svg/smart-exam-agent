-- 四、回填省份字典的题数缓存（如果你已经把题目灌进 questions 表）
-- 没灌可以跳过这步，灌完后单独跑一次
-- ============================================================
UPDATE `provinces` p
LEFT JOIN (
    SELECT `province`, COUNT(*) AS cnt
    FROM `questions`
    WHERE `is_valid` = 1
    GROUP BY `province`
) q ON p.code = q.province
SET p.total_papers = COALESCE(q.cnt, 0);


